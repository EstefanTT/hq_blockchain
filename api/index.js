// api/index.js
// Express app setup — middleware, file-based route loading, error handling.

import express from 'express';
import loadRoutes from './utils/loadRoutes.js';
import errorHandler from './middlewares/errorHandler.js';

// ####################################################################################################################################
// ##########################################################   SETUP   ###############################################################
// ####################################################################################################################################

const app = express();

app.use(express.json());

// ####################################################################################################################################
// ##########################################################   ROUTES   ##############################################################
// ####################################################################################################################################

await loadRoutes(app, global.path.apiRoutes, '/api');

// ####################################################################################################################################
// ######################################################   ERROR HANDLING   ##########################################################
// ####################################################################################################################################

app.use(errorHandler);

export default app;
